package com.michaelgatesdev.OldLeaf.game;

import java.io.File;

public final class Acre
{
    // ============================================================================================================================================ \\
    
    private byte value;
    private File file;
    
    
    // ============================================================================================================================================ \\
    
    
    public Acre()
    {
    }
    
    // ============================================================================================================================================ \\
    
    
    public void setImageFile(File file)
    {
        this.file = file;
    }
    
    
    public void setValue(byte value)
    {
        this.value = value;
    }
    
    // ============================================================================================================================================ \\
    
    
    public File getImageFile()
    {
        return file;
    }
    
    
    public short getValue()
    {
        return value;
    }
    
    // ============================================================================================================================================ \\
}
